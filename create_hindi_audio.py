from gtts import gTTS

text = """
नमस्कार।

कवच एआई में आपका स्वागत है।

कृपया अपना ओटीपी, पिन, सीवीवी और बैंकिंग पासवर्ड किसी के साथ साझा न करें।

संदिग्ध लिंक पर क्लिक करने से पहले उसकी जांच अवश्य करें।

साइबर धोखाधड़ी होने पर तुरंत 1930 पर कॉल करें या साइबर क्राइम पोर्टल पर शिकायत दर्ज करें।

सतर्क रहें, सुरक्षित रहें।

धन्यवाद।
"""

tts = gTTS(text=text, lang="hi")
tts.save("kavach_hindi.mp3")

print("Hindi Audio Created Successfully")